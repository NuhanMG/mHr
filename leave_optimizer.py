# LEAVE WINDOW FINDER
# ============================================================================

def _is_free_day(d: date) -> bool:
    """Check if a date is a weekend or holiday (no personal leave needed)."""
    return d.weekday() >= 5 or d in HOLIDAY_DATES  # 5=Sat, 6=Sun


def _get_day_type(d: date) -> str:
    """Get the type label for a date."""
    if d in HOLIDAY_DATES:
        return f"🎉 {HOLIDAY_NAMES[d]}"
    elif d.weekday() == 5:
        return "🔵 Weekend (Saturday)"
    elif d.weekday() == 6:
        return "🔵 Weekend (Sunday)"
    else:
        return "📝 Personal Leave"


def find_optimal_leave_windows(
    days_wanted: int,
    target_month: Optional[int] = None,
    max_results: int = 3,
) -> List[Dict]:
    """
    Find optimal leave windows that maximize days off with minimum personal leave.
    
    Scans sliding windows across the calendar to find periods where holidays
    and weekends cluster together, minimizing the need for personal leave days.
    
    Args:
        days_wanted: Minimum total consecutive days off desired
        target_month: Optional month to restrict search (1-12)
        max_results: Maximum number of suggestions to return
        
    Returns:
        List of window dicts sorted by efficiency (best first), each containing:
        - start_date, end_date
        - total_days, personal_leave_needed, free_days
        - efficiency (ratio of total_days / personal_leave_needed)
        - day_breakdown: list of (date, type_label) tuples
    """
    year = HOLIDAY_PLANNER_YEAR
    today = date.today()
    
    # Define search range
    if target_month:
        start = date(year, target_month, 1)
        # End of the target month
        if target_month == 12:
            end = date(year, 12, 31)
        else:
            end = date(year, target_month + 1, 1) - timedelta(days=1)
    else:
        # Search the full year but only future dates
        start = max(date(year, 1, 1), today)
        end = date(year, 12, 31)
    
    # Don't suggest past dates unless in target month mode
    if not target_month:
        start = max(start, today)
    
    windows = []
    
    # Try different window sizes (from days_wanted up to days_wanted + 5)
    for window_size in range(days_wanted, days_wanted + 6):
        current = start
        while current + timedelta(days=window_size - 1) <= end:
            window_end = current + timedelta(days=window_size - 1)
            
            # Calculate days breakdown
            breakdown = []
            personal_leave = 0
            free_days = 0
            
            for i in range(window_size):
                d = current + timedelta(days=i)
                day_type = _get_day_type(d)
                breakdown.append((d, day_type))
                
                if _is_free_day(d):
                    free_days += 1
                else:
                    personal_leave += 1
            
            # Only include windows with at least some free days (holidays/weekends)
            # and reasonable personal leave usage
            if free_days > 0 and personal_leave <= days_wanted:
                efficiency = window_size / max(personal_leave, 0.5)  # Higher is better
                
                windows.append({
                    "start_date": current,
                    "end_date": window_end,
                    "total_days": window_size,
                    "personal_leave_needed": personal_leave,
                    "free_days": free_days,
                    "efficiency": efficiency,
                    "day_breakdown": breakdown,
                })
            
            current += timedelta(days=1)
    
    # Sort by: 1) fewest personal leave days, 2) highest total days, 3) earliest
    windows.sort(key=lambda w: (w["personal_leave_needed"], -w["total_days"], w["start_date"]))
    
    # Deduplicate overlapping windows - keep only the best per cluster
    filtered = []
    used_ranges = set()
    for w in windows:
        # Check if this window overlaps significantly with already selected ones
        window_dates = set()
        for i in range(w["total_days"]):
            window_dates.add(w["start_date"] + timedelta(days=i))
        
        overlap = window_dates & used_ranges
        if len(overlap) < len(window_dates) * 0.5:  # Less than 50% overlap
            filtered.append(w)
            used_ranges.update(window_dates)
        
        if len(filtered) >= max_results:
            break
    
    return filtered


