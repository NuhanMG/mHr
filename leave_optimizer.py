"""
Mobitel HR Assistant - Smart Leave Optimizer
Suggests optimal leave dates by combining holidays, weekends, and personal leave
to maximize time off with minimum leave days used.

Features:
- Leave intent detection from natural language queries
- Holiday-aware calendar window finding
- Efficiency-based ranking (best ratio of days off to leave used)
- Month-specific and general suggestions
"""

import re
import json
from datetime import date, timedelta
from typing import Optional, List, Dict, Tuple
from config import setup_logging, BASE_DIR

logger = setup_logging("hr_assistant.leave")

# ============================================================================
# HOLIDAY DATA - Loaded from holidays.json (auto-generated by holiday_parser.py)
# ============================================================================

HOLIDAYS_JSON_PATH = BASE_DIR / "holidays.json"


def load_holidays() -> Tuple[int, list]:
    """
    Load holiday data from holidays.json.
    
    Returns:
        Tuple of (year, list of (date, name) tuples).
        Returns (current_year, []) if file doesn't exist.
    """
    if not HOLIDAYS_JSON_PATH.exists():
        logger.warning(f"holidays.json not found at {HOLIDAYS_JSON_PATH}")
        logger.warning("Run 'python holiday_parser.py' to generate it from the Holiday Planner PDF")
        return date.today().year, []
    
    try:
        with open(HOLIDAYS_JSON_PATH, "r", encoding="utf-8") as f:
            data = json.load(f)
        
        year = data.get("year", date.today().year)
        holidays = []
        
        for entry in data.get("holidays", []):
            date_str = entry["date"]
            name = entry["name"]
            parts = date_str.split("-")
            d = date(int(parts[0]), int(parts[1]), int(parts[2]))
            holidays.append((d, name))
        
        logger.info(f"Loaded {len(holidays)} holidays for year {year} from holidays.json")
        return year, holidays
    
    except Exception as e:
        logger.error(f"Error loading holidays.json: {e}")
        return date.today().year, []


# Load holidays at module level
HOLIDAY_PLANNER_YEAR, _HOLIDAY_LIST = load_holidays()
HOLIDAYS = {HOLIDAY_PLANNER_YEAR: _HOLIDAY_LIST}

# Set of holiday dates for fast lookup
HOLIDAY_DATES = {d for d, _ in HOLIDAYS.get(HOLIDAY_PLANNER_YEAR, [])}
HOLIDAY_NAMES = {d: name for d, name in HOLIDAYS.get(HOLIDAY_PLANNER_YEAR, [])}

# Month name mappings
MONTH_NAMES = {
    "january": 1, "jan": 1, "february": 2, "feb": 2, "march": 3, "mar": 3,
    "april": 4, "apr": 4, "may": 5, "june": 6, "jun": 6,
    "july": 7, "jul": 7, "august": 8, "aug": 8, "september": 9, "sep": 9, "sept": 9,
    "october": 10, "oct": 10, "november": 11, "nov": 11, "december": 12, "dec": 12,
}


# ============================================================================
# LEAVE INTENT DETECTION
# ============================================================================

def detect_leave_intent(query: str) -> Optional[Dict]:
    """
    Detect if the user's query is asking for leave/holiday suggestions.
    
    Returns:
        Dict with 'days_wanted' and 'target_month' if leave intent detected,
        None otherwise.
    """
    query_lower = query.lower().strip()
    
    # Keywords that indicate leave suggestion intent
    leave_keywords = [
        "suggest", "recommend", "best day", "best time", "good day", "good time",
        "when should i take", "when to take", "plan leave", "plan my leave",
        "long weekend", "days off", "day off", "leave plan", "leave suggest",
        "holiday plan", "maximize", "optimal leave", "suitable day", "suitable date",
    ]
    
    # Must also have a leave/holiday context word
    context_keywords = [
        "leave", "holiday", "off", "vacation", "break", "rest", "time off",
    ]
    
    has_leave_intent = any(kw in query_lower for kw in leave_keywords)
    has_context = any(kw in query_lower for kw in context_keywords)
    
    if not (has_leave_intent and has_context):
        return None
    
    logger.info("Leave suggestion intent detected")
    
    result = {
        "days_wanted": None,
        "target_month": None,
    }
    
    # Extract number of days wanted
    day_patterns = [
        r'(\d+)\s*(?:day|days)',
        r'(\d+)\s*(?:day|days)\s*(?:leave|off|holiday)',
    ]
    for pattern in day_patterns:
        match = re.search(pattern, query_lower)
        if match:
            result["days_wanted"] = int(match.group(1))
            break
    
    # Check for "long weekend" → 3-4 days
    if result["days_wanted"] is None and "long weekend" in query_lower:
        result["days_wanted"] = 4
    
    # Default to 4 days if no specific number given
    if result["days_wanted"] is None:
        result["days_wanted"] = 4
    
    # Extract target month
    for month_name, month_num in MONTH_NAMES.items():
        if month_name in query_lower:
            result["target_month"] = month_num
            break
    
    # Check for "this month" / "next month"
    today = date.today()
    if "this month" in query_lower:
        result["target_month"] = today.month
    elif "next month" in query_lower:
        next_m = today.month + 1 if today.month < 12 else 1
        result["target_month"] = next_m
    
    logger.info(f"  Days wanted: {result['days_wanted']}, Target month: {result['target_month']}")
    return result


