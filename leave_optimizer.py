# RESPONSE FORMATTING
# ============================================================================

def format_leave_suggestion(intent: Dict, windows: List[Dict]) -> str:
    """
    Format leave suggestions into a user-friendly response.
    
    Args:
        intent: The detected intent dict
        windows: List of optimal leave windows
        
    Returns:
        Formatted string response
    """
    if not windows:
        month_text = ""
        if intent.get("target_month"):
            month_names_rev = {v: k.capitalize() for k, v in MONTH_NAMES.items() if len(k) > 3}
            month_text = f" in {month_names_rev.get(intent['target_month'], 'that month')}"
        return (
            f"I couldn't find a good leave window{month_text} with the holidays available. "
            f"Try adjusting the number of days or checking a different month."
        )
    
    days = intent.get("days_wanted", 4)
    lines = [f"Here are the best options to get **{days}+ consecutive days off** using the fewest personal leave days:\n"]
    
    for i, w in enumerate(windows, 1):
        start_str = w["start_date"].strftime("%a, %d %b %Y")
        end_str = w["end_date"].strftime("%a, %d %b %Y")
        
        lines.append(f"**Option {i}: {start_str} → {end_str}**")
        lines.append(f"📅 Total days off: **{w['total_days']}** | "
                     f"Personal leave needed: **{w['personal_leave_needed']}** | "
                     f"Free days: **{w['free_days']}**\n")
        
        for d, dtype in w["day_breakdown"]:
            day_str = d.strftime("%a %d %b")
            lines.append(f"  - {day_str} — {dtype}")
        
        lines.append("")  # blank line between options
    
    lines.append("💡 *Tip: Days marked with 🎉 are company holidays and 🔵 are weekends — no leave needed!*")
    
    return "\n".join(lines)


# ============================================================================
# MAIN ENTRY POINT
# ============================================================================

def handle_leave_query(query: str) -> Optional[str]:
    """
    Main entry point for leave-related queries.
    
    Args:
        query: User's question
        
    Returns:
        Formatted leave suggestion if leave intent detected, None otherwise
    """
    intent = detect_leave_intent(query)
    if intent is None:
        return None
    
    logger.info(f"Processing leave suggestion: {intent}")
    
    windows = find_optimal_leave_windows(
        days_wanted=intent["days_wanted"],
        target_month=intent.get("target_month"),
    )
    
    logger.info(f"Found {len(windows)} optimal leave windows")
    for i, w in enumerate(windows):
        logger.debug(
            f"  Window {i+1}: {w['start_date']} to {w['end_date']} "
            f"({w['total_days']} days, {w['personal_leave_needed']} leave needed)"
        )
    
    return format_leave_suggestion(intent, windows)
